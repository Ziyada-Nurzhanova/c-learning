int dayOfWeek = 5;
switch (dayOfWeek)
{
    case 1:
    MessageBox.Show("Monday");
    break;
    case 2:
    MessageBox.Show("Tuesday");
    case 3:
    MessageBox.Show("Wednesday");
    case 4:
    MessageBox.Show("Thursday");
    case 5:
    MessageBox.Show("Friday");
    case 6:
    MessageBox.Show("Sunday");
    case 7:
    MessageBox.Show("Saturday");
    default:
    MessageBox.Show("invalid value");
}